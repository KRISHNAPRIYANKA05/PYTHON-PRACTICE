"""\n =new line
\t = new tab
\\ = backslash
\" = double quotes
 """
#example usage 
name = "ramu"
age = "12"
print(f"my name is {name} \nand my age is{age}")
print(f"my name is {name} \tand my age is{age}")
print(f"my name is {name} \\and my age is{age}")
print(f"my name is {name} and my age is{age}")